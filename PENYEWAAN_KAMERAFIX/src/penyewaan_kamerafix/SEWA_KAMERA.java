    private void TxtuangbayarKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_TxtuangbayarKeyPressed
        // TODO add your handling code here:
        if (evt.getKeyCode()==KeyEvent.VK_ENTER){
            int bayar = Integer.parseInt(Txttotalbayar.getText());
            int uangbayar =Integer.parseInt(Txtuangbayar.getText());
            int uangkembalian = uangbayar - bayar;
            Txtuangkembalian.setText(Integer.toString(uangkembalian));

        }
    }//GEN-LAST:event_TxtuangbayarKeyPressed

