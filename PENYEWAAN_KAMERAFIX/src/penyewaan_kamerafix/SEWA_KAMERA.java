    private void tombolcariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tombolcariActionPerformed
        // TODO add your handling code here:
        String cari = caritext.getText();
        //ambil nilai jumlah baris yang ada pada tabel
        int jumlahbaris = tbdata.getRowCount();
        //LAKUKAN PERULANGAN UNTUK PENCARIAN
         for (int i = 0; i <jumlahbaris; i++) {
            if(cari.equals(tbdata.getValueAt(i, 0))) {
                JOptionPane.showMessageDialog(null,
                        "NOMOR ID SEWA YANG ANDA CARI BERADA DI BARIS KE-" + (i+1));

                break;
            }
         
                
            }
            
          
    }//GEN-LAST:event_tombolcariActionPerformed

